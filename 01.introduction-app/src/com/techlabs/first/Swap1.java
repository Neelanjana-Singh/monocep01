package com.techlabs.first;

public class Swap1 {

	public static void main(String[] args) {
		 int number1=12;
		 int number2=21;
		 int temp;
		 System.out.println("Number before swap :"+number1+" "+number2);
		 temp=number1;
		 number1=number2;
		 number2=temp;
		 System.out.println("Number after swap :"+number1+" "+number2);

	}

}
